<h1>L'association</h1>
<h5>Depuis 10 ans à St-Malo, ville emblématique Bretonne, 
l'association Mal'Fest organise un festival de musique accessible et éclectique. Sa force réside dans une équipe d'organisateurs 100% bénévoles, animée depuis leur adolescence par l'envie de dynamiser le territoire.
L'association Mal'Fest propose lors de son festival éponyme, une programmation artistique qui défend la 
découverte en associant des artistes locaux à ses têtes d'affiche. </h5>

<h2><u>Les valeurs de notre projet</u></h2>

<h5 class='rose'>1. L'inclusion</h5>
<p>Le Mal'Fest' met en place des actions pour favoriser l’accessibilité à la fois physique et sociale de son événement. 
  Notre partenariat avec l’association <a href=https://questhandi.fr/>Quest’handi</a> permet une accesibilité aux lieux et concerts aux personnes 
  nécessistantes. Rendre le festival financièrement accessible est également un de nos objectifs à travers 
  l’organisation de vente à prix avantageux au sein des lycées ou notre adhésion au dispositif pass culture. </p>

<h5 class='rose'>2. La jeunesse</h5>
<p>Mobiliser les jeunes du territoire dans l’organisation d’un événement culturel est l'un de nos objectif principal. 
  Les bénévoles de l'association sont âgés de 16 à 30 ans et prennent plaisir à s'engager et se responsabiliser autour du festival. 
  Adhérents mais surtout acteurs essentiels du projet, 
  ils sont aujourd’hui 50 à s’investir et à rendre cet événement unique.</p>

<h5 class='rose'>3. Le territoire</h5>
<p>Devenu un rendez-vous incontournable du pays de Saint-Malo et plus largement du département, 
  le Mal'Fest a pour objectif de prendre une part importante dans la région en diffusant son identité singulière. 
  Notre idée ? Permettre aux bretilliens et au malouins d’assister à des concerts de qualité tout près de chez eux !</p>
